// INITIALISE THE BASIS FOR THE PROVIDED INSTRUCTION
// ACCESS THE WORD ENCOMPASSING THE INSTRUCTION AND IT'S RESPECTIVE ADDRESS
void CATH_DSP_INSTRUCTION_INIT(SH_DSP_INSTRUCTION* INSTR, U32 WORD, U32 ADDRESS)
{
    INSTR->WORD = WORD;
    INSTR->FLAGS = 0;
    INSTR->DESCRIPTOR = &DSP_DESCRIPTORS[CATH_INSTR_ID_DSP_INVALID];
    INSTR->INSTR_ID = CATH_INSTR_ID_DSP_INVALID;
    INSTR->CATEGORY = CATH_INSTRCAT_DSP;
    INSTR->PC = ADDRESS;
    INSTR->IS_PARALLEL = false;
    INSTR->D_SLOT.IS_ACTIVE = false;
    INSTR->D_SLOT.MNEMONIC = NULL;
    INSTR->D_SLOT.DEST_NAME = NULL;
    INSTR->D_SLOT.SRC_NAME = NULL;
    INSTR->D_SLOT.SOURCE = DSP_OPERAND_NONE;
}

// STATICALLY AVAILABLE EMISSION FOR THE D SLOT INTO THE BUFFER
// HANDLES ALL OF THE COMBINATORIAL INSTRUCTION PATTERNS
// ON THE ASSUMPTION OF THE OPERAND TYPE BEING A STORE
static UNK CATH_DSP_EMIT_DBUS(const SH_DSP_INSTRUCTION* INSTR, char* BUFFER)
{
    if(INSTR->D_SLOT.SOURCE == DSP_OPERAND_STORE)
    {
        return snprintf(BUFFER, BUFFER ? 64 : 0,
            "%s, %s, %s",
            INSTR->D_SLOT.MNEMONIC,
            INSTR->D_SLOT.SRC_NAME,
            INSTR->D_SLOT.DEST_NAME);
    }

    else
    {
        U8 SIMM = (U8)SCU_DSP_GET_D_SIMM(INSTR);
        return snprintf(BUFFER, BUFFER ? 64 : 0,
            "%s #0x%02X, %s",
            INSTR->D_SLOT.MNEMONIC,
            SIMM,
            INSTR->D_SLOT.DEST_NAME);
    }
}

// DETERMINE THE SIZE OF THE CURRENT INSTRUCTION IN THE OUTPUT BUFFER
// WITHOUT WRITING TO IT ACCORDINGLY
//
// MIRRORS THE DESIGN PATTERNS OF CATH_INSTRUCTION_GET_SIZE 
// USED BY CALLERS THAT NEED TO ALLOCATE BEFORE WRITING
UNK CATH_DSP_INSTRUCTION_GET_SIZE(const SH_DSP_INSTRUCTION* INSTR)
{
    UNK TOTAL_SIZE = 0;
    TOTAL_SIZE += strlen(CATH_GET_DSP_OPCODE_NAME(INSTR->INSTR_ID));

    // ASSUME THAT A NOP HAVE NO OPERAND FIELD AND RETURN WITH
    // A GRACEFUL FALLBACK
    if(INSTR->DESCRIPTOR->IS_NOP || INSTR->DESCRIPTOR->IS_END)
    {
        if(INSTR->IS_PARALLEL && INSTR->D_SLOT.IS_ACTIVE)
        {
            TOTAL_SIZE += 2;
            TOTAL_SIZE += CATH_DSP_EMIT_DBUS(INSTR, NULL);
        }

        return TOTAL_SIZE;
    }

    TOTAL_SIZE += 1;
    TOTAL_SIZE += CATH_DSP_INSTRUCTION_DISASM_OPERAND(INSTR, NULL);

    if(INSTR->IS_PARALLEL && INSTR->D_SLOT.IS_ACTIVE)
    {
        TOTAL_SIZE += 2;
        TOTAL_SIZE += CATH_DSP_EMIT_DBUS(INSTR, NULL);
    }

    return TOTAL_SIZE;
}
